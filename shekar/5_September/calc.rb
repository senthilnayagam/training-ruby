#Program to implement simple calculator.
class Clac
    def self.add(n1,n2)
        return n1+n2
    end
    def self.sub(n1,n2)
        return n1-n2
    end
    def self.mul(n1,n2)
        return n1*n2
    end
    def self.div(n1,n2)
        return n1/n2
    end
    def self.pow(n1,n2)
        return n1**n2
    end
end
